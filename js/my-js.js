$('.show-btn').click(function() {
	  $( ".navbar-items" ).slideToggle("slow");
	  $( ".button-close" ).slideToggle("slow");
	  $( ".show-btn" ).slideToggle(1200);
	});
$('.button-close').click(function() {
	  $( ".navbar-items" ).slideToggle("slow");
	  $( ".button-close" ).slideToggle("slow");
	  $( ".show-btn" ).slideToggle(1200);
	});